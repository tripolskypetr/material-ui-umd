    export const list = ({
      limit = 10,
      offset = 0,
      order = 'asc',
      orderBy = '',
      keyword = '',
    }) => {

      console.log({limit, offset, order, orderBy, keyword})

      let copy = peoples;

      if (keyword) {
        copy = copy.filter(({firstName, lastName}) => `${firstName}${lastName}`.includes(keyword));
      }

      if (order && orderBy) {
        copy = copy.sort((a, b) => `${a[orderBy]}`.localeCompare(`${b[orderBy]}`));
        if (order === 'desc') {
          copy = copy.reverse();
        }
      }

      return {
        items: peoples.slice(offset).slice(0, limit),
        limit, offset,
        total: copy.length
      };
    };
